void strokeOutput(GLfloat x,GLfloat y,float fact,char *format,...) {
  va_list args;
  char buffer[2000],*p;
  va_start(args,format);
  vsprintf(buffer,format,args);
  va_end(args);
  glPushMatrix();
  glTranslatef(x,y,0);
  glScalef(0.005*fact,0.005*fact,0.005*fact);
  float w;
  glGetFloatv(GL_LINE_WIDTH,&w);
  GLboolean b;
  glGetBooleanv(GL_BLEND,&b);
  GLboolean ls;
  glGetBooleanv(GL_LINE_SMOOTH,&ls);
  if ( bold )
    glLineWidth(2.0);
    else
    glLineWidth(1.0);
  if ( anti ) {
    glEnable(GL_BLEND);
    glBlendFunc(GL_SRC_ALPHA,GL_ONE_MINUS_SRC_ALPHA);
    glEnable(GL_LINE_SMOOTH); }
    else {
    glDisable(GL_BLEND);
    glDisable(GL_LINE_SMOOTH); }
  for ( p = buffer ; *p ; p++ ) {
    glutStrokeCharacter(GLUT_STROKE_ROMAN,*p);
    glTranslatef(ecar,0.0F,0.0F); }
  if ( b )
    glEnable(GL_BLEND);
    else
    glDisable(GL_BLEND);
  if ( ls )
    glEnable(GL_LINE_SMOOTH);
    else
    glDisable(GL_LINE_SMOOTH);
  glLineWidth(w);
  glPopMatrix();
}
